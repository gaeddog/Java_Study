package ch02.unit04;

public class Ex07_Excape {

	public static void main(String[] args) {
		// 확장열
		
		System.out.println("a\tb\tc"); // \t : 탭
		System.out.println("a\ny"); // \n : 엔터
		System.out.println("1\\2"); // \\ : \ 출력
		System.out.println("\101"); // 8진수 101에 해당하는 문자 :A
		System.out.println("\u0041"); // 유니코드 0041(16진수의) 문자 :A
		

	}

}
